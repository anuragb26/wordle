const findWord = () =>
  WORDLIST[Math.round(Math.random() * WORDLIST.length)].toUpperCase();

const useRandomWord = () => {
  const [word, setWord] = useState(findWord());
  const setRandomWord = useCallback(() => {
    setWord(findWord());
  }, [setWord]);
  return [word, setRandomWord];
};

export default useRandomWord;
